    private void baca_data() {
        try {
            stm = Con.createStatement();
            RsBrg = stm.executeQuery("select * from kecamatan");

            ResultSetMetaData meta = RsBrg.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while (RsBrg.next()) {
                baris = RsBrg.getRow();
            }

            dataTable = new Object[baris][col];
            int x = 0;
            RsBrg.beforeFirst();
            while (RsBrg.next()) {
                dataTable[x][0] = RsBrg.getInt("id");
                dataTable[x][1] = getNmKabKO(RsBrg.getInt("id_kab"));
                dataTable[x][2] = RsBrg.getString("kec");
                x++;
            }
            tblKec.setModel(new DefaultTableModel(dataTable, header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void detail() {
        try (ResultSet rs = stm.executeQuery("SELECT * FROM kota")) {
            while (rs.next()) {
                cmbKabKo.addItem(rs.getString("kota"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error loading : " + e.getMessage());
        }
    }

    private String getNmKabKO(int id) {
        String nama = "";
        try {
            stm = Con.createStatement();
            ResultSet sql = stm.executeQuery("select kota from kota where id=" + id);
            if (sql.next()) {
                nama = sql.getString("kota");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return nama;
    }

    private int getIdKabKo(String nama) {
        int id = 0;
        try {
            stm = Con.createStatement();
            ResultSet sql = stm.executeQuery("select id from kota where kota='" + nama + "'");
            if (sql.next()) {
                id = sql.getInt("id");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return id;
    }

