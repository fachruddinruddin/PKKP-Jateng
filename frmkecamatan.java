    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String tkab = (String) cmbKabKo.getSelectedItem();
        int tId = getIdKabKo(tkab);
        String tNama = txtKec.getText();
        try {
            if (edit == true) {
                stm.executeUpdate("update kecamatan set id_kab='" + tId + "', kec='" + tNama + "'where id =" + id);
            } else {
                stm.executeUpdate("INSERT into kecamatan (id_kab, kec) VALUES(" + tId + ",'" + tNama + "')");
            }
            tblKec.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

